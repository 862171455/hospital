package org.java.entity;

public class Yaocar {
    private Integer yaocarId;

    private Integer yaocarNum;

    private Integer yaocarBr;

    private Integer yaocarYao;

    public Integer getYaocarId() {
        return yaocarId;
    }

    public void setYaocarId(Integer yaocarId) {
        this.yaocarId = yaocarId;
    }

    public Integer getYaocarNum() {
        return yaocarNum;
    }

    public void setYaocarNum(Integer yaocarNum) {
        this.yaocarNum = yaocarNum;
    }

    public Integer getYaocarBr() {
        return yaocarBr;
    }

    public void setYaocarBr(Integer yaocarBr) {
        this.yaocarBr = yaocarBr;
    }

    public Integer getYaocarYao() {
        return yaocarYao;
    }

    public void setYaocarYao(Integer yaocarYao) {
        this.yaocarYao = yaocarYao;
    }
}