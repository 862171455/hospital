package org.java.entity;

public class Beed {
    private Integer bedId;

    private Integer bedRoomId;

    private Integer bedType;

    private Integer bedMoney;

    public Integer getBedId() {
        return bedId;
    }

    public void setBedId(Integer bedId) {
        this.bedId = bedId;
    }

    public Integer getBedRoomId() {
        return bedRoomId;
    }

    public void setBedRoomId(Integer bedRoomId) {
        this.bedRoomId = bedRoomId;
    }

    public Integer getBedType() {
        return bedType;
    }

    public void setBedType(Integer bedType) {
        this.bedType = bedType;
    }

    public Integer getBedMoney() {
        return bedMoney;
    }

    public void setBedMoney(Integer bedMoney) {
        this.bedMoney = bedMoney;
    }
}